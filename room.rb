class Room
  attr_accessor(:layout)

  def initialize(layout_arr)
    @layout = layout_arr
    @f1 = MyImg::Floor1
    @f2 = MyImg::Floor2
    @w1 = MyImg::Wall1
    @exit = Exit.new(576, 128)
  end

  def draw
    @layout.each_with_index do |tileArr, r|
      tileArr.each_with_index do |tile, c|

        if (tile == "w1")
          @w1.draw(c*64, r*64, 0)
        elsif (tile == "f2")
          @f2.draw(c*64, r*64, 0)
        else
          puts("no tile like that exists")
        end

      end
    end
  end

end # END Room Class


